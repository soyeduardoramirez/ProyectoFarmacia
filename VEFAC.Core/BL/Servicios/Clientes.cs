            {
                conexion.Open();
                SqlCommand sqlCommand = new SqlCommand();
                sqlCommand.Connection = conexion;
                sqlCommand.CommandText = "[dbo].[Sp_Clientes]";
                sqlCommand.CommandType = System.Data.CommandType.StoredProcedure;
                #region parametros de Entradas
                sqlCommand.Parameters.AddWithValue("@opcion", "AgregarCliente");
                sqlCommand.Parameters.AddWithValue("@nombre_cliente", clientesResponse.nombre_cliente);
                sqlCommand.Parameters.AddWithValue("@apellido_cliente", clientesResponse.apellido_cliente);
                sqlCommand.Parameters.AddWithValue("@direccion", clientesResponse.direccion);
